#in python we have 5 types of operators i.e
#1: Arithmatic operators
#2: Assignment operators
#3: Relational operators
#4: Logical operators
#5: Unary operators


#Arithmatic Operators:
x = 10
y = 5
print(x+y)
print(x-y)
print(x*y)
print(x/y)
#Assignment Operators:
z = 50
z += 2
print(z)
z *= 2
print(z)
z /= 2
print(z)
z -= 2
print(z)
#Unary Operators:
n = 7
print(n)
print(-n)
n = -n
print(n)

#Relational operators
print(x == y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)
print(x != y)

#Logical Operators
print(x > 2 and y < 10)
print(x > 2 or y < 1)
print(not x)