#complement(~) Operators
print(~12)
#bitwise & operator
print(24 & 13)
#bitwise or operator
print(14 | 13)
#bitwise xor operator
print(12 | 1)
#bitwise left shift operator
#10 = 1010.0000.. after left shif it will 101000.00.. i.e = 40
print(10 << 2)
#bitwise right shift operator
#10 = 1010.0000.. after right shif it will 1010.0000.. i.e = 2
print(10 >> 2)
